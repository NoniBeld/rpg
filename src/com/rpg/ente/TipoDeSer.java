package com.rpg.ente;

public enum TipoDeSer { 
	ActoPuro, 		// Dios ser sin principio ni fin el es y al final seguira siendo el.
	ActosSimples, 	// Seres abstractos que necesitan un nombre pero no necesariamente un cuerpo,
					// el cual tiene una funcion unica con un principio pero sin un final
					// como, el amor, la fe, la esperanza, el caos, la nada,
	ActoCompuesto, 	// Seres Abstractos que son mas leyes y/o rglas intrinsecas en el universo,
					// que tienen un principio pero estan sujetas a un final casi infinito, 
					// como el tiempo, el orden, el espacio, los alineamientos, la Gravedad, la energia  
	SerSimple, 		// seres con un cuerpo simple, creado de una sola cosa, 
					// con funciones especificas que pueden cambiar, con un principio pero sin un final Intrinseco
					// como Los Angeles, Los elementales, los Espiritus  
	SerCompuesto, 	// seres con un cuerpo comlejo creado de dos o mas substancias, 
					// con funciones especifcas con un principio y un final especifico.
					// Como las plantas, Hongos, Bacterias, Animales
	
}
